<?php

namespace Woof\Util;

/**
 * オブジェクトを JSON や XML などのデータフォーマットにシリアライズ (変換) 可能であることを示すインタフェースです。
 *
 * 独自のエンティティや値オブジェクトにこのインタフェースを実装することで、そのオブジェクトを
 * JsonBody などに渡して安全にデータ変換できるようになります。
 */
interface DataObject
{
    /**
     * 各種データフォーマットに変換するための、元となる値を取得します。
     *
     * @return mixed 変換の元となる中間データ (通常は配列やスカラー値)
     */
    public function toValue();
}
